class swap
{
	public static void main(String[] args) {
		int a = 10;
		int b = 12;
		int temp;
		System.out.println("Before swapping the values are: "+a +b);
		// System.out.println(a b);
		temp = a;
		a = b;
		b = temp;
		System.out.println("After swapping the values are: " +a +b);
		// System.out.println(a b);
	}
}